# variables.py
# 4 types simples de variable:

# 1/ str, chaîne de caractères, délimitées par les guillemets (simple ou double)
group = "TSRS 19"
print(group)
print(type(group))

# 2/ int
group_num = 19
print(group_num)
print(type(group_num))

# 3/ float
moyenne = 23.6
print(moyenne)
print(type(moyenne))

# 4/ bool
isGroupFull = True
print(isGroupFull)
print(type(isGroupFull))



